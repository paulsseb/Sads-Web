Rheumatoid arthritis (RA) is an autoimmune condition that causes chronic inflammation. With RA, your immune system attacks your bodyâ€™s tissues and causes painful swelling of the joints. Without treatment, RA can severely damage joints. <br> <br>
	
	 
There are many ways that rheumatoid arthritis can appear, but some of the most recognized signs are in the hands and feet. However, many different joints can be affected, with varying severity. <br> <br>

Imaging tests, like X-ray, computed tomography (CT) scan, and magnetic resonance imaging (MRI) help doctors take a close look at your joints and assess any damage. <br> <br>

RA can also affect other parts of the body, including skin, blood vessels, eyes, and lungs. People with RA may deal with fatigue and general weakness as well.
