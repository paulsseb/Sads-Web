<p>
 According to the Arthritis Association Uganda, For a person in Uganda to be diagnosed with arthritis, they are required to go to the only rheumatology clinic in the country at Mulago Referal hospital in Kiruddu of which this clinic is open only once a week that is on Fridays. <br> 
<br>
	
	 
On top of this, the clinic currently has only a single doctor who gets overwhelmed by the number of patients. Very little is known about this disease by health practitioners and patients alike causing the society  and those affected to struggle blindly with some
